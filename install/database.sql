BEGIN TRANSACTION;
DROP TABLE IF EXISTS "cups";
CREATE TABLE "cups" (
	"id"	INTEGER,
	"name"	TEXT NOT NULL,
	"type"	TEXT,
	PRIMARY KEY("id" AUTOINCREMENT)
);
DROP TABLE IF EXISTS "difficulty_modes";
CREATE TABLE "difficulty_modes" (
	"id"	INTEGER,
	"name"	TEXT,
	PRIMARY KEY("id" AUTOINCREMENT)
);
DROP TABLE IF EXISTS "drivers";
CREATE TABLE "drivers" (
	"id"	INTEGER,
	"name"	TEXT NOT NULL,
	"phone"	TEXT,
	"image"	TEXT NOT NULL,
	"wins"	INTEGER DEFAULT 0,
	"losses"	INTEGER DEFAULT 0,
	"active"	TEXT DEFAULT 'false',
	PRIMARY KEY("id" AUTOINCREMENT)
);
DROP TABLE IF EXISTS "game_cup";
CREATE TABLE "game_cup" (
	"game_id"	INTEGER,
	"cup_id"	INTEGER,
	PRIMARY KEY("game_id","cup_id")
);
DROP TABLE IF EXISTS "game_difficulty";
CREATE TABLE "game_difficulty" (
	"game_id"	INTEGER,
	"mode_id"	INTEGER,
	PRIMARY KEY("game_id","mode_id")
);
DROP TABLE IF EXISTS "games";
CREATE TABLE "games" (
	"id"	INTEGER,
	"name"	TEXT NOT NULL,
	PRIMARY KEY("id" AUTOINCREMENT)
);
DROP TABLE IF EXISTS "matches";
CREATE TABLE "matches" (
	"id"	INTEGER,
	"driver_id"	INTEGER,
	"score"	INTEGER,
	"bracket_level"	INTEGER,
	"match_num"	INTEGER,
	PRIMARY KEY("id" AUTOINCREMENT)
);
DROP TABLE IF EXISTS "settings";
CREATE TABLE "settings" (
	"id"	INTEGER,
	"name"	TEXT NOT NULL,
	"value"	TEXT NOT NULL,
	PRIMARY KEY("id" AUTOINCREMENT)
);
